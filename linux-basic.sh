# Basic Linux commands (Using Bash)

# Change directory
cd
# Move / rename 
mv
# Create a directory
mkdir
# List directory contents
ls
# Remove a file
rm
# Remove a directory
rmdir
# Copy a file
cp
# Copy one or more files
cat
# View file without opening it
less
# View file without opening it (less is better)
more
# Create a file
touch
# Print working directory
pwd

# Switch to root user (super user)
sudo su -

# Show all files even hidden ones
ls -a